package com.example.anamuslim.pojo.quran;

public class QuranPart {

    String number_in_surah;
    String page_number;
    String surah_number;
    String part_number;

    public String getNumber_in_surah() {
        return number_in_surah;
    }

    public void setNumber_in_surah(String number_in_surah) {
        this.number_in_surah = number_in_surah;
    }

    public String getPage_number() {
        return page_number;
    }

    public void setPage_number(String page_number) {
        this.page_number = page_number;
    }

    public String getSurah_number() {
        return surah_number;
    }

    public void setSurah_number(String surah_number) {
        this.surah_number = surah_number;
    }

    public String getPart_number() {
        return part_number;
    }

    public void setPart_number(String part_number) {
        this.part_number = part_number;
    }
}
